const dicts: Record<Locale, Dict> = { en, ko }

function interpolate(s: string, vars?: Record<string, string | number>) {
  if (!vars) return s
  return s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`))
}

export function useT() {
  const locale = useAppStore((s) => s.locale)
  return (key: string, vars?: Record<string, string | number>) => {
    const d = dicts[locale] ?? en
    const raw = d[key] ?? en[key] ?? key
    return interpolate(raw, vars)
  }
}

export function t(locale: Locale, key: string, vars?: Record<string, string | number>) {
  const raw = (dicts[locale] ?? en)[key] ?? en[key] ?? key
  return interpolate(raw, vars)
}
